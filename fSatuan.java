    private void BikinJudulKolom(){
        Object[] judul_kolom={("ID"),("Nama Satuan"),("Keterangan")};
        DefaultTableModel model = new DefaultTableModel(null,judul_kolom);
        jTable1.setModel(model);
        model.getDataVector().removeAllElements();
       
        try 
        {
            koneksiDB.koneksi();
            koneksiDB.rs=koneksiDB.st.executeQuery("select * from satuan");
            while (koneksiDB.rs.next()) {
                Object[] data={koneksiDB.rs.getString("id_satuan"),
                                koneksiDB.rs.getString("nm_satuan"),
                                koneksiDB.rs.getString("keterangan")};
                model.addRow(data);
                }
            koneksiDB.putuskoneksi();
        }catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Menampilkan data gagal! Error: "+e);
        }
        lebarkolom();
    }
    
